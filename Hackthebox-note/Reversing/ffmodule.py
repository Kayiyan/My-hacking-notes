def ROL(data, shift, size=32):
    shift %= size
    remains = data >> (size - shift)
    body = (data << shift) - (remains << size )
    return (body + remains)

flag = ''

for i in range(len(enc)):
    x = (enc[i] + 0xed) & 0xFF
    y = ROL(x, 3, 8)
    flag += chr(y ^ 0x42)

